public class WrapperClass {
    public static void main(String[] args) {
        //wrapper class = provides way to use primitive data types as ref data types
        //primitive     //wrapper
        //------------------------
        // boolean      //Boolean
        // char         //Character
        // int          //Integer
        //double        //Double

        //autoboxing = automatic conversion Java compiler makes between primitive and corresponding wrapper class
        //unboxing = reverse autoboxing
        //allows ref data type as primitive type and vice versa

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Brian";


    }
}
